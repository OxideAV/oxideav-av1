#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn idct64_dc_reconstruction_constant() {
        let mut x = [0i32; 64];
        x[0] = 65536;
        idct64(&mut x);
        let first = x[0];
        for (i, v) in x.iter().enumerate().skip(1) {
            assert_eq!(*v, first, "non-constant at {i}");
        }
        assert_ne!(first, 0);
    }

    #[test]
    fn idct64_zero_is_zero() {
        let mut x = [0i32; 64];
        idct64(&mut x);
        for &v in &x {
            assert_eq!(v, 0);
        }
    }

    #[test]
    fn idct64_linearity_within_tolerance() {
        let mut a = [0i32; 64];
        let mut b = [0i32; 64];
        for i in 0..64 {
            a[i] = ((i * 17) as i32 % 200) - 100;
            b[i] = ((i * 11) as i32 % 150) - 75;
        }
        let mut sum = [0i32; 64];
        for i in 0..64 {
            sum[i] = a[i] + b[i];
        }
        let mut ac = a;
        let mut bc = b;
        idct64(&mut ac);
        idct64(&mut bc);
        idct64(&mut sum);
        for i in 0..64 {
            let want = ac[i] + bc[i];
            let diff = (sum[i] - want).abs();
            assert!(diff <= 60, "linearity at {i}: diff {diff}");
        }
    }
}
