//! Coefficient CDF tables that depend on the per-frame Q context
//! (`COEFF_CDF_Q_CTXS = 4`).
//!
//! These three tables are spec-defined as four-dimensional
//! (`[COEFF_CDF_Q_CTXS][…]`) in AV1 §9.4 / Annex E "Additional tables":
//!
//! - `Default_Txb_Skip_Cdf[4][TX_SIZES=5][TXB_SKIP_CONTEXTS=13][3]`
//! - `Default_Dc_Sign_Cdf[4][PLANE_TYPES=2][DC_SIGN_CONTEXTS=3][3]`
//! - `Default_Coeff_Base_Eob_Cdf[4][TX_SIZES=5][PLANE_TYPES=2]`
//!   `[SIG_COEF_CONTEXTS_EOB=4][4]` (named `DEFAULT_COEFF_BASE_EOB_MULTI_CDF`
//!   here to match the libaom-style identifier used by `tools/gen_cdfs`).
//!
//! The upstream `goavif` reference package (and the auto-generated
//! `generated.rs` it drives) stores these tables flattened against the
//! outer Q-context axis — i.e. only the `q_ctx = 0` slice survives. That
//! is a latent dimensionality bug for non-lossless streams where the
//! per-frame `q_ctx = q_index_to_ctx(base_q_idx)` resolves to 1, 2, or
//! 3 (§9.4.2 step 1). The decoder then read the wrong CDF slice and
//! mis-decoded coefficient sign / EOB / skip symbols.
//!
//! This module is the hand-transcribed fix. Every value is taken
//! directly from `docs/video/av1/av1-spec.txt` — no reference codec
//! (dav1d / libaom / SVT-AV1 / rav1d / goavif) was consulted. The
//! `q_ctx = 0` slice is byte-identical to the values previously emitted
//! by the generator (verified against `generated.rs`), so existing
//! lossless / `base_q_idx = 0` regression coverage is preserved.
//!
//! Wire format matches the rest of [`crate::cdfs`]:
//! - `cdf[0..N-2]` = `32768 - spec_value[i]` (survival values, monotone
//!   decreasing)
//! - `cdf[N-1]` = `0` sentinel
//! - `cdf[N]` = `0` update counter (starts at 0)
//!
//! The DC-sign table is identical across all four `q_ctx` slices in the
//! spec — preserved here verbatim rather than collapsed, so the index
//! plumbing in `decode::coeffs::CoeffCdfBank::new` / encoder twin can
//! stay symmetric with the other two tables.
//!
//! `tools/gen_cdfs` skips the three names below (see its
//! `OVERRIDE_NAMES` list); regenerating `generated.rs` will not
//! clobber these definitions.

