/// 176×144 still-picture KEY frame (reduced sequence header,
/// `allow_screen_content_tools = 1`, `allow_intrabc = 1`, 128×128
/// superblocks, `base_q_idx = 160`) with ONE `use_intrabc == 1` leaf —
