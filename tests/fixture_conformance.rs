/// Same stream through the driver twice — the decode is deterministic
/// and holds no cross-call state.
#[test]
fn spec_driver_is_deterministic() {
    let ivf = unhex(TINY_I_ONLY_16X16_IVF);
    let a = decode_av1_spec(&ivf).unwrap();
    let b = decode_av1_spec(&ivf).unwrap();
    assert_eq!(a, b);
}

/// Truncated IVF payloads surface a typed error, never a panic. (A cut
/// at exactly the 32-byte IVF file-header boundary is the one
/// well-formed prefix: an IVF with zero frame records decodes to zero
/// frames.)
#[test]
fn spec_driver_rejects_truncated_input() {
    let ivf = unhex(TINY_I_ONLY_16X16_IVF);
    for cut in [0, 10, 31, 44, 60, ivf.len() - 1] {
        assert!(
            decode_av1_spec(&ivf[..cut]).is_err(),
            "truncation at {cut} must be rejected"
        );
    }
    assert_eq!(
        decode_av1_spec(&ivf[..32]).unwrap(),
        vec![],
        "header-only IVF decodes to zero frames"
    );
}

/// Expand an RLE-hex literal — a sequence of `(value: u8, count: u16)`
/// triples, each 6 hex chars — back to raw bytes. Used for the larger
/// expected-plane payloads, which are long-run flat regions.
fn unrle(s: &str) -> Vec<u8> {
    assert!(s.len() % 6 == 0, "RLE literal must be (u8, u16) triples");
    let mut out = Vec::new();
    for i in 0..s.len() / 6 {
        let v = u8::from_str_radix(&s[6 * i..6 * i + 2], 16).unwrap();
        let n = usize::from_str_radix(&s[6 * i + 2..6 * i + 6], 16).unwrap();
        out.extend(std::iter::repeat(v).take(n));
    }
    out
}

fn assert_decodes_byte_exact_rle(name: &str, ivf_hex: &str, expected_rle: &str, frames: usize) {
    let expected = unrle(expected_rle);
    let expected_hex: String = expected.iter().map(|b| format!("{b:02x}")).collect();
    assert_decodes_byte_exact(name, ivf_hex, &expected_hex, frames);
}

