// emit writes one `pub static NAME: <type> = <value>;` declaration.
// Walks the reflect.Value recursively:
//
//   - cdfs.CDF (= []uint16)       → `[u16; N]`
//   - [N]cdfs.CDF                 → `[&'static [u16]; N]` (jagged: inner
//     CDFs in the same outer array can
//     have different lengths)
//   - [N][M]cdfs.CDF etc.         → nested array of the above.
func emit(out *os.File, name string, v reflect.Value) {
	rustName := rustifyName(name)
	shape, innerLen, jagged := describeShape(v)
	typeStr := rustType(shape, innerLen, jagged)

	fmt.Fprintf(out, "pub static %s: %s = ", rustName, typeStr)
	emitValue(out, v, 0)
	fmt.Fprintln(out, ";")
	fmt.Fprintln(out, "")
}

// describeShape inspects the outer array dimensions up to the leaf CDF.
// Returns the list of outer dims (excluding the inner uint16 axis), the
// inner length if uniform across all leaves (0 if jagged), and a flag
// for the jagged case.
func describeShape(v reflect.Value) (dims []int, innerLen int, jagged bool) {
	dims = []int{}
	t := v.Type()
	for t.Kind() == reflect.Array {
		dims = append(dims, t.Len())
		t = t.Elem()
	}
	// t is now the leaf: cdfs.CDF (= []uint16) or []uint16.
	lengths := map[int]struct{}{}
	visitLeaves(v, func(leaf reflect.Value) {
		lengths[leaf.Len()] = struct{}{}
	})
	if len(lengths) == 1 {
		for L := range lengths {
			innerLen = L
		}
	} else {
		jagged = true
	}
	return
}

func visitLeaves(v reflect.Value, fn func(reflect.Value)) {
	if v.Kind() == reflect.Slice {
		fn(v)
		return
	}
	if v.Kind() == reflect.Array {
		for i := 0; i < v.Len(); i++ {
			visitLeaves(v.Index(i), fn)
		}
		return
	}
}

func rustType(dims []int, _ int, _ bool) string {
	// Leaf is always `&'static [u16]` — lets jagged tables (mixed CDF
	// lengths, e.g. partition) coexist with uniform ones in the same
	// declaration, and keeps the emitted syntax uniform for a simple
	// scalar CDF.
	s := "&'static [u16]"
	for i := len(dims) - 1; i >= 0; i-- {
		s = fmt.Sprintf("[%s; %d]", s, dims[i])
	}
	return s
}

func emitValue(out *os.File, v reflect.Value, depth int) {
	indent := strings.Repeat("    ", depth)
	if v.Kind() == reflect.Slice {
		// Leaf CDF: emit as `&[a, b, c, 0, 0]`.
		fmt.Fprint(out, "&[")
		for i := 0; i < v.Len(); i++ {
			if i > 0 {
				fmt.Fprint(out, ", ")
			}
			fmt.Fprintf(out, "%d", v.Index(i).Uint())
		}
		fmt.Fprint(out, "]")
		return
	}
	if v.Kind() == reflect.Array {
		// Outer array.
		fmt.Fprint(out, "[\n")
		for i := 0; i < v.Len(); i++ {
			fmt.Fprint(out, indent+"    ")
			emitValue(out, v.Index(i), depth+1)
			fmt.Fprint(out, ",\n")
		}
		fmt.Fprint(out, indent+"]")
		return
	}
	panic(fmt.Sprintf("unexpected kind %v", v.Kind()))
}

// rustifyName converts DefaultPartitionCDF → DEFAULT_PARTITION_CDF.
func rustifyName(name string) string {
	var b strings.Builder
	for i, r := range name {
		if i > 0 && r >= 'A' && r <= 'Z' {
			prev := rune(name[i-1])
			if !(prev >= 'A' && prev <= 'Z') {
				b.WriteRune('_')
			} else if i+1 < len(name) {
				next := rune(name[i+1])
				if next >= 'a' && next <= 'z' {
					b.WriteRune('_')
				}
			}
		}
		b.WriteRune(r)
	}
	return strings.ToUpper(b.String())
}
