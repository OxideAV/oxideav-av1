// gen_cdfs imports github.com/KarpelesLab/goavif/av1/entropy/cdfs and
// emits a single Rust source file containing every Default*CDF table as
// a `pub static` of the appropriately jagged shape. Run after updating
// goavif to resync the tables:
//
//	go run ./tools/gen_cdfs > src/cdfs/generated.rs
//
// The goavif cdfs package exports wire-format CDFs (each CDF is a
// `[]uint16` of length N+1 where the final slot is the update counter
// starting at 0). We copy the bytes verbatim — no interpretation —
// keeping the oxideav-av1 range coder API identical to goavif's.
package main

import (
	"fmt"
	"os"
	"reflect"
	"sort"
	"strings"

	"github.com/KarpelesLab/goavif/av1/entropy/cdfs"
)

func main() {
	out := os.Stdout
	fmt.Fprintln(out, "// THIS FILE IS GENERATED BY tools/gen_cdfs. DO NOT EDIT BY HAND.")
	fmt.Fprintln(out, "// Re-run: `go run ./tools/gen_cdfs > src/cdfs/generated.rs`")
	fmt.Fprintln(out, "// Source: github.com/KarpelesLab/goavif/av1/entropy/cdfs (MIT)")
	fmt.Fprintln(out, "//")
	fmt.Fprintln(out, "// Tables are wire-format: `cdf[0..N-2]` = Q15 P(X>i)*32768 (decreasing),")
	fmt.Fprintln(out, "// `cdf[N-1]` = 0 sentinel, `cdf[N]` = update counter (starts at 0).")
	fmt.Fprintln(out, "")
	fmt.Fprintln(out, "#![allow(clippy::all)]")
	fmt.Fprintln(out, "#![allow(dead_code)]")
	fmt.Fprintln(out, "")

	// Reflect into the cdfs package via a small registry. We can't
	// enumerate package-level vars directly, so list them explicitly.
	// Additions should be mirrored here and in the test that checks
	// round-trip byte counts.
	registry := registerCDFs()
	names := make([]string, 0, len(registry))
	for n := range registry {
		names = append(names, n)
	}
	sort.Strings(names)

	for _, n := range names {
		emit(out, n, registry[n])
	}
}

